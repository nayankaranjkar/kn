public class oddnumber8
{  
public static void main(String args[])   
{
	System.out.print("List of odd numbers from 1 to 100 ");  
	for (int i=1; i<=100; i++)   
	{  
//logic to check if the number is odd or not  
//if i%2 is not equal to zero, the number is odd  
		if (i%2!=0)   
		{  
			System.out.print(i + " ");  
		}  
	}  
}  
}

