public class ASCII3
{
	public static void main(String[] args)
		{
			char ch1='C';
			char ch2='d';
			int  num=8;


			int ascii1=ch1;
			int ascii2=ch2;
			int ascii3=num+48;


			System.out.println("The ASCII value of " + ch1 + " is: " + ascii1);  
			System.out.println("The ASCII value of " + ch2 + " is: " + ascii2); 
			System.out.println("The ASCII value of " + num + " is: " + ascii3); 
		}
}